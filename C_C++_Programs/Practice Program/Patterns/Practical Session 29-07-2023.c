#include<stdio.h>
int main()
{
	int limit,num,i,count;
	
	printf("Enter the limit :\n");
	scanf("%d",&limit);          //100
	
	for(num=1;num<=limit;num++)  //2<=100 true num=7
	{                            //
		count=0;                 //count=0
		for(i=2;i<=num/2;i++)    //2<=7/2 true
		{                        //
			if(num%i==0)         //7%2==1
			{                    //
				count++;         //count=1
				break;           //
			}                    //
		}                        //
		if(count==0 && num!=1)   //count=0 && 7!=1
		{                        //1&&1
			printf("%d\t",num);  //2 3 5 7
		}                        //
	}                            //
	return 0;                    //
}

